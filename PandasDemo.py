#import the pandas library and aliasing as pd
import pandas as pd
import numpy as np
data = np.array(['Silver','Gold','Platinum','Diamond'])
s = pd.Series(data)
print (s)




#creating index
s = pd.Series(data,index=[100,101,102,103])
print (s)
#create series from dictionary
data = {'Silver' : 0., 'Gold' : 1., 'Platinum' : 2.}
s = pd.Series(data)
print (s)


#Data Frame
data = [['Alex',10],['Bob',12],['Clarke',13]]
